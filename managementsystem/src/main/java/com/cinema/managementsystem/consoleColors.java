package com.cinema.managementsystem;

public class ConsoleColors {
    // Reset
    public static final String ANSI_RESET = "\u001B[0m";

    // Cor Única para a Logo
    public static final String ANSI_BRIGHT_RED = "\u001B[91m"; // Dourado Metálico

    // Cores Principais CINEMA
    public static final String ANSI_RED_RUBY = "\u001B[31m"; // Vermelho Rubi
    public static final String ANSI_GOLD = "\u001B[33;1m"; // Dourado Brilhante
    public static final String ANSI_BLACK = "\u001B[30m"; // Preto

    // Cores para o rolo de filme CINEMA
    public static final String ANSI_BEIGE = "\u001B[38;5;230m"; // Bege Areia
    public static final String ANSI_DARK_RED = "\u001B[31;1m";   // Vermelho Escuro
    public static final String ANSI_LIGHT_GRAY = "\u001B[37m";   // Cinza Claro

    // Cores da Estrutura do Sistema
    public static final String ANSI_SILVER = "\u001B[37m"; // Cinza Prata
    public static final String ANSI_WHITE_SNOW = "\u001B[97m"; // Branco Neve

    // Cor para a outras Áreas
    public static final String ANSI_LIGHT_BLUE = "\u001B[94m"; // Azul Claro
    public static final String ANSI_GREEN = "\u001B[32m"; // Verde
    public static final String ANSI_LIGHT_GREEN = "\u001B[92m"; // Verde Claro
    

    

}
